#include "delay.h"

/*********************************************************************************************
* 名称：delay_count
* 功能：计数延时
* 参数：times------计数数值
* 返回：无
* 修改：无
*********************************************************************************************/
void delay_count(uint32_t times)
{
  uint32_t temp = 0;                                            //定义临时变量并初始化
  
  while(times --){                                              //times自减并判断是否为空
    temp = 1000;                                                //赋值1000
    while(temp --);                                             //temp自减并判断是否为空
  }
}